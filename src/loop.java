public class loop {
    public static void main(String args[]){
        int i =0;
       while(i<=4){
           System.out.println("Hrithik Gorane");
           i++;
       }

       //do-while loop use.
        int a=0;
        do{
            System.out.println("happy");
            a++;
        }while(a<=4);

        //for-loop using.
        for(int num=0;num<=4; num++){
            System.out.println("having good day!");
        }
    }
}
